  private onChange: (value: string) => void = () => {};
  private onTouched: () => void = () => {};

  writeValue(value: string): void {
    this.value = value || '';
  }

  registerOnChange(fn: (value: string) => void): void {
    this.onChange = fn;
  }

  registerOnTouched(fn: () => void): void {
    this.onTouched = fn;
  }

  onValueChange(value: string): void {
    this.value = value;
    this.onChange(value);
    this.valueChange.emit(value);
  }

  applyFormat(format: string): void {
    const textareaEl = this.textarea.nativeElement;
    const start = textareaEl.selectionStart;
    const end = textareaEl.selectionEnd;
    const selectedText = this.value.substring(start, end);
    const beforeText = this.value.substring(0, start);
    const afterText = this.value.substring(end);

    let newText = '';
    let cursorOffset = 0;

    switch (format) {
      case 'bold':
        newText = `**${selectedText || 'bold text'}**`;
        cursorOffset = selectedText ? newText.length : 2;
        break;
      case 'italic':
        newText = `*${selectedText || 'italic text'}*`;
        cursorOffset = selectedText ? newText.length : 1;
        break;
      case 'h1':
        newText = `# ${selectedText || 'Heading 1'}`;
        cursorOffset = newText.length;
        break;
      case 'h2':
        newText = `## ${selectedText || 'Heading 2'}`;
        cursorOffset = newText.length;
        break;
      case 'h3':
        newText = `### ${selectedText || 'Heading 3'}`;
        cursorOffset = newText.length;
        break;
      case 'ul':
        newText = `- ${selectedText || 'list item'}`;
        cursorOffset = newText.length;
        break;
      case 'ol':
        newText = `1. ${selectedText || 'list item'}`;
        cursorOffset = newText.length;
        break;
      case 'code':
        newText = `\`${selectedText || 'code'}\``;
        cursorOffset = selectedText ? newText.length : 1;
        break;
      case 'quote':
        newText = `> ${selectedText || 'quote'}`;
        cursorOffset = newText.length;
        break;
      case 'hr':
        newText = '\n---\n';
        cursorOffset = newText.length;
        break;
    }

    this.value = beforeText + newText + afterText;
    this.onValueChange(this.value);

    // Set cursor position after format is applied
    setTimeout(() => {
      const newCursorPos = start + cursorOffset;
      textareaEl.focus();
      textareaEl.setSelectionRange(newCursorPos, newCursorPos);
    }, 0);
  }

  insertEmoji(emoji: string): void {
    const textareaEl = this.textarea.nativeElement;
    const start = textareaEl.selectionStart;
    const end = textareaEl.selectionEnd;
    const beforeText = this.value.substring(0, start);
    const afterText = this.value.substring(end);

    this.value = beforeText + emoji + afterText;
    this.onValueChange(this.value);

    // Close emoji picker and set cursor position after emoji
    this.showEmojiPicker = false;
    this.emojiSearchTerm = '';
    setTimeout(() => {
      const newCursorPos = start + emoji.length;
      textareaEl.focus();
      textareaEl.setSelectionRange(newCursorPos, newCursorPos);
    }, 0);
  }

  getFilteredEmojis(): { name: string; emojis: string[] }[] {
    if (!this.emojiSearchTerm.trim()) {
      return this.emojiCategories;
    }

    const searchLower = this.emojiSearchTerm.toLowerCase();
    return this.emojiCategories
      .map(category => ({
        ...category,
        emojis: category.emojis.filter(emoji => {
          // Simple search - could be enhanced with emoji names/keywords
          return category.name.toLowerCase().includes(searchLower);
        })
      }))
      .filter(category => category.emojis.length > 0);
  }
